package ejercicios;

public class Ejercicio4App {

	public static void main(String[] args) {

		int N = 10;

		System.out.println("Valor de N = " + N);

		N += 77;
		System.out.println("(N + 77) = " + N);

		N -= 3;
		System.out.println("(N - 3) = " + N);

		N *= 2;
		System.out.println("(N * 2) = " + N);
	}
}